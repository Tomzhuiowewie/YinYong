#include<iostream>
using namespace std;

//交换函数

//1. 值传递
void mySwap01(int a, int b)
{
	int temp = a;
	a = b;
	b = temp;
}

//2. 地址传递
void mySwap02(int * a, int * b)
{
	int temp = *a;
	*a = *b;
	*b = temp;
}


//3. 引用传递
void mySwap03(int &a, int &b)
{
	int temp = a;
	a = b;
	b = temp;
}
int main03() {

	int a = 100;
	int b = 200;
	//mySwap01(a, b);//值传递，形参不会修饰实参
	//mySwap02(&a, &b);//地址传递，形参修饰实参
	mySwap03(a, b);//引用传递，形参修饰实参


	cout << "a = " << a << endl;
	cout << "b = " << b << endl;

	system("pause");

	return 0;
}